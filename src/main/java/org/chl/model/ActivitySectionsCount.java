package org.chl.model;

public class ActivitySectionsCount {
    private Integer challengeRequestCount;
    private  Integer followerRequestCount;
    private  Integer newFriendSuggestionCount;
    private  Integer challengeApproveCount;

    public Integer getChallengeRequestCount() {
        return challengeRequestCount;
    }

    public void setChallengeRequestCount(Integer challengeRequestCount) {
        this.challengeRequestCount = challengeRequestCount;
    }

    public Integer getFollowerRequestCount() {
        return followerRequestCount;
    }

    public void setFollowerRequestCount(Integer followerRequestCount) {
        this.followerRequestCount = followerRequestCount;
    }

    public Integer getNewFriendSuggestionCount() {
        return newFriendSuggestionCount;
    }

    public void setNewFriendSuggestionCount(Integer newFriendSuggestionCount) {
        this.newFriendSuggestionCount = newFriendSuggestionCount;
    }

    public Integer getChallengeApproveCount() {
        return challengeApproveCount;
    }

    public void setChallengeApproveCount(Integer challengeApproveCount) {
        this.challengeApproveCount = challengeApproveCount;
    }
}
